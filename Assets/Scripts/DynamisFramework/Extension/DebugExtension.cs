using System.Collections;
using System.Collections.Generic;
using UnityEngine;


/// <summary>
/// Debugクラスの拡張クラス
/// </summary>
public static class DebugExtension
{
    //=====================================================================================================================
    // 内部クラス・列挙型定義
    //=====================================================================================================================

    //=====================================================================================================================
    // 定数
    //=====================================================================================================================

    //=====================================================================================================================
    // 変数
    //=====================================================================================================================

    //=====================================================================================================================
    // プロパティ
    //=====================================================================================================================

    //=====================================================================================================================
    // Private関数
    //=====================================================================================================================

    //=====================================================================================================================
    // Public関数
    //=====================================================================================================================

    public static void ColorLogWarning(this Debug self, Object message)
    {
        Debug.LogWarning($"<color=yellow>{message}</color>");
    }

    public static void ColorLogError(this Debug self, Object message)
    {
        Debug.LogError($"<color=red>{message}</color>");
    }


} // DebugExtension
