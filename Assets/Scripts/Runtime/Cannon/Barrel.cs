using System.Collections;
using System.Collections.Generic;
using UnityEngine;

namespace CannonShooting
{
    /// <summary>
    /// 砲台定義
    /// </summary>
    public class Barrel
    {
        //=====================================================================================================================
        // 内部クラス・列挙型定義
        //=====================================================================================================================

        //=====================================================================================================================
        // 定数
        //=====================================================================================================================

        //=====================================================================================================================
        // 変数
        //=====================================================================================================================
        private Transform _barrelTrsf;

        //=====================================================================================================================
        // プロパティ
        //=====================================================================================================================

        //=====================================================================================================================
        // コンストラクタ
        //=====================================================================================================================
        public Barrel(Transform barrelTrsf)
        {
            _barrelTrsf = barrelTrsf;
        }

        //=====================================================================================================================
        // Private関数
        //=====================================================================================================================

        //=====================================================================================================================
        // Public関数
        //=====================================================================================================================

        /// <summary>
        /// バレルの回転定義
        /// </summary>
        /// <param name="value"></param>
        public void Rotate(Vector3 value)
        {
            _barrelTrsf.rotation = Quaternion.Euler(value);
        }

    } // class Barrel
}// namespace CannonShooting
