using UnityEngine;

namespace CannonShooting
{
    /// <summary>
    /// 台座の定義
    /// </summary>
    public class LowerCarriage
    {
        //=====================================================================================================================
        // 内部クラス・列挙型定義
        //=====================================================================================================================

        //=====================================================================================================================
        // 定数
        //=====================================================================================================================

        //=====================================================================================================================
        // 変数
        //=====================================================================================================================
        private Transform _lowerCarriageTrsf;

        //=====================================================================================================================
        // プロパティ
        //=====================================================================================================================

        //=====================================================================================================================
        // コンストラクタ
        //=====================================================================================================================
        public LowerCarriage(Transform lowerCarriageTrsf)
        {
            _lowerCarriageTrsf = lowerCarriageTrsf;
        }

        //=====================================================================================================================
        // Private関数
        //=====================================================================================================================

        //=====================================================================================================================
        // Public関数
        //=====================================================================================================================
        
        /// <summary>
        /// 台座の回転定義
        /// </summary>
        /// <param name="value"></param>
        public void Rotate(Vector3 value)
        {
            _lowerCarriageTrsf.rotation = Quaternion.Euler(value);
        }

    } // class LowerCarriage
}// namespace CannonShooting
