using System.Collections;
using System.Collections.Generic;
using UnityEngine;


/// <summary>
/// コマンド実装クラス
/// </summary>
public class ConcreteCommand1 : ICommand
{
    //=====================================================================================================================
    // 内部クラス・列挙型定義
    //=====================================================================================================================

    //=====================================================================================================================
    // 定数
    //=====================================================================================================================

    //=====================================================================================================================
    // 変数
    //=====================================================================================================================
    private IReceiver _receiver;

    //=====================================================================================================================
    // プロパティ
    //=====================================================================================================================

    //=====================================================================================================================
    // コンストラクタ
    //=====================================================================================================================
    public ConcreteCommand1(IReceiver receiver)
    {
        _receiver = receiver;
    }

    //=====================================================================================================================
    // Private関数
    //=====================================================================================================================

    //=====================================================================================================================
    // Public関数
    //=====================================================================================================================
    public void Execute()
    {
        _receiver.ReceiveMethod1();
    }

} // ConcreteCommand
