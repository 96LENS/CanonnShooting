using System.Collections;
using System.Collections.Generic;
using UnityEngine;


/// <summary>
/// Commandの実装クラス
/// </summary>
public class ConcreteCommand2 : ICommand
{
    //=====================================================================================================================
    // 内部クラス・列挙型定義
    //=====================================================================================================================

    //=====================================================================================================================
    // 定数
    //=====================================================================================================================

    //=====================================================================================================================
    // 変数
    //=====================================================================================================================
    private IReceiver _receiver;

    //=====================================================================================================================
    // プロパティ
    //=====================================================================================================================

    //=====================================================================================================================
    // コンストラクタ
    //=====================================================================================================================
    public ConcreteCommand2(IReceiver receiver) =>  _receiver = receiver;

    //=====================================================================================================================
    // Private関数
    //=====================================================================================================================

    //=====================================================================================================================
    // Public関数
    //=====================================================================================================================
    public void Execute()
    {
        _receiver.ReceiveMethod2();
    }

} // ConcreteCommand
